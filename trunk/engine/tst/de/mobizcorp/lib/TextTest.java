    /*
     * Test method for 'de.mobizcorp.lib.Text.getUnicode(byte[], int)'
     */
    public void testGetUnicodeByteArrayInt() throws UnsupportedEncodingException {
        assertEquals("zero", 0, Text.getUnicode(new byte[] {0}, 0));
        assertEquals("one", 1, Text.getUnicode(new byte[] {1}, 0));
        assertEquals("one[1]", 1, Text.getUnicode(new byte[] {0,1,2}, 1));
        // 110x xxx 10xx xxxx (00 0100 0001 / 0x41)
        assertEquals("0x41[0]", 0x41, Text.getUnicode(new byte[] {(byte) 0xC1,(byte) 0x81,2}, 0));
        assertEquals("0x41[1]", 0x41, Text.getUnicode(new byte[] {(byte) 0xC1,(byte) 0x81,2}, 1));
        final String probe = "水星";
        byte[] bytes = probe.getBytes("UTF-8");
        assertEquals("水星[0]", probe.charAt(0), Text.getUnicode(bytes, 0));
        assertEquals("水星[1]", probe.charAt(0), Text.getUnicode(bytes, 1));
        assertEquals("水星[2]", probe.charAt(0), Text.getUnicode(bytes, 2));
        assertEquals("水星[3]", probe.charAt(1), Text.getUnicode(bytes, 3));
        assertEquals("水星[4]", probe.charAt(1), Text.getUnicode(bytes, 4));
        assertEquals("水星[5]", probe.charAt(1), Text.getUnicode(bytes, 5));
    }

